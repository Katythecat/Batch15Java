package day7;

public class ForLoops5 {
    public static void main(String[] args) {
        //white table of 5
        for(int i = 1 ; i <= 10 ; i++){
            System.out.println("5 * "+i+" = "+(5*i));

            for(int j = 1 ; j<=10 ; j++){
                System.out.println("3 * "+j+" = "+(3*j));
            }
        }
    }
}
