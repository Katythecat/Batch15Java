package class8;

public class RecapDemo2 {
    public static void main(String[] args) {
        for (int i = 10; i >= 2; i -= 2) {
            System.out.println(i);
        }


        int i = 1;
        do {
            System.out.println("Hello World!");
            i++;

        } while (i <= 5);

    }
}