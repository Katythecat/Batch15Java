package class3;

public class RelationalOperator_6 {
    public static void main(String[] args) {

        /*
       Arithmetic operators
       int + int = int
       int/int => int
       int - int = int

         */

        /*
        Relational Operators
        >
        =>
        ==
        !=
        =<
        <
         */

        int number = 10;
        int number2 = 10;
        System.out.println(number+number2);

        boolean result = 20>10; // always boolean
        System.out.println(result);

        System.out.println(20>10);//true
        System.out.println(5>10);//false
        System.out.println(5!=10);//true  not equal to !=
        System.out.println(5==10);//false   equal to ==
        System.out.println(5==15);//true   equal to ==
        System.out.println(15<=15);//true
        System.out.println(10<=10);//true






    }
}
